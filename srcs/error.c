#include "../includes/fdf.h"

void		error_load(t_map *m)
{
	int		i;

	i = 0;
	while (i < m->h - 1)
	{
		if (count(m->m[0]) != count(m->m[i]))
		{
			ft_putendl(ESCAPE_YODA);
			exit(9);
		}
		i++;
	}
}
